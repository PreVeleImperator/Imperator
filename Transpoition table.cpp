	void Entry (uint64_t index, uint64_t code, int value, int nodeType, int depth, int fromI, int toI, int moveType)
	{
		Table &entry = table [index];

		if (entry.code == 0)
		{
			entry.code     = code;
			entry.value    = value;
			entry.nodeType = nodeType;
			entry.depth    = depth;

			entry.   fromI =    fromI;
			entry.     toI =      toI;
			entry.moveType = moveType;
		}
		else if (entry.code == code  &&  depth > entry.depth)
		{
			entry.value    = value;
			entry.nodeType = nodeType;
			entry.depth    = depth;

			entry.   fromI =    fromI;
			entry.     toI =      toI;
			entry.moveType = moveType;
		}
	}

	int KnownPosition (uint64_t index, uint64_t code, int depth, int alfa, int beta)
	{
		Table &entry = table [index];

		if (entry.code == code  &&  depth <= entry.depth)
		{
			switch (entry.nodeType)
			{
				case  PV_NODE: return entry.value;
				case CUT_NODE: return entry.value >= beta ? beta : NO_ENTRY;
				case ALL_NODE: return entry.value <= alfa ? alfa : NO_ENTRY;
			}
		}
		else
		{
			return NO_ENTRY;
		}
	}

